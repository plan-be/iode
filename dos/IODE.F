ACTION start
    {
    APPL_RUN ode_appl
    }

APPL ode_appl  {
    line 0 col 0 nl 25 nc 80
    FILL ' '
    BACK BLUE
    BEGIN_FN {
	HELPFILE $HELPFILE
	C_FN {{extern int first_page;
	      if(first_page == 0) SCR_record_key(SCR_A_Z);}}
	}
    A_BAR ode_ab
    FNKEY {
	CTRL_L  ACTION ode_lock
    }

    DRAW
	FILL RED        0 0 1 80
	FILL REVERSE    1 0 1 80
	FILL reverse    24 0 1 80
	"IODE - Integrated ODE tools                         ³        ³        ³"  REVERSE 24 0

	$IODE_VERSION RED 0 8
}

A_BAR ode_ab
{
	LINE 1 COL 2
	OPTION "Modeling tools" ALT_M HELP "Modeling software"
	{
	    PG_SLIDE enp_mod
	    C_FN EN_start(1);
	}
	COMMENT "Enter ODE modeling software"

	OPTION "Import/Export"   ALT_I HELP "Import/export"
	{
	    PG_SLIDE enp_imp
	    C_FN EN_start(2);
	}
	COMMENT "Import and export ODE Workspaces"

	OPTION "Translate/Print" ALT_T HELP "Translate"
	{
	    PG_SLIDE enp_tra
	    C_FN EN_start(4);
	}
	COMMENT "Translate and print output files produced by ODE"

	OPTION "Misc" ALT_M HELP "MiscIODE"  COMMENT "Miscellaneous functions"
	    MENU
	    {
		OPTION "Info"               ALT_Z
		    COMMENT "Version number"
		    {PG_SLIDE odep_info}

		OPTION "User's guide    F1"
		    COMMENT "Pop user's guide on screen"
		    {RECORD {F1}}

		OPTION "Shell DOS       ALT_D" ALT_D
		    COMMENT "Dos command processor, type exit at DOS prompt to resume"
		    {system "cls;command"}

		OPTION "Quit            ALT-X" ALT_X
		    {ASK "Quit ODE integrator (Yes/No) ?" APPL_QUIT}
		    COMMENT "Quit program"
	     }
}

